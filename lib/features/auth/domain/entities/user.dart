class User {
  final String id;
  final String fullName;
  final String username;
  final String email;
  final String phone;
  final String avatar;
  final String token;

  User({
    required this.id,
    required this.fullName,
    required this.username,
    required this.email,
    required this.phone,
    required this.avatar,
    required this.token
  });
}

